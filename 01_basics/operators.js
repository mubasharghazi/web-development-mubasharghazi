let x = 5;
let y = 2;

console.log(x + y); // 7
console.log(x ** y); // 25 (exponent)
console.log(x % y); // 1 (modulus)
console.log(2 == '2');  // true (loose equality, type coerced)
console.log(2 === '2'); // false (strict equality, no coercion)
